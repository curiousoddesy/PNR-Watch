import Joi from 'joi';

// User registration validation schema
export const userRegistrationSchema = Joi.object({
  email: Joi.string()
    .email()
    .required()
    .messages({
      'string.email': 'Please provide a valid email address',
      'any.required': 'Email is required'
    }),
  password: Joi.string()
    .min(8)
    .pattern(new RegExp('^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[@$!%*?&])[A-Za-z\\d@$!%*?&]'))
    .required()
    .messages({
      'string.min': 'Password must be at least 8 characters long',
      'string.pattern.base': 'Password must contain at least one uppercase letter, one lowercase letter, one number, and one special character',
      'any.required': 'Password is required'
    }),
  name: Joi.string()
    .min(2)
    .max(50)
    .pattern(/^[a-zA-Z\s]+$/)
    .required()
    .messages({
      'string.min': 'Name must be at least 2 characters long',
      'string.max': 'Name cannot exceed 50 characters',
      'string.pattern.base': 'Name can only contain letters and spaces',
      'any.required': 'Name is required'
    }),
  notificationPreferences: Joi.object({
    emailEnabled: Joi.boolean().default(true),
    pushEnabled: Joi.boolean().default(false),
    inAppEnabled: Joi.boolean().default(true),
    statusTypes: Joi.array()
      .items(Joi.string().valid('confirmation', 'waitlist_movement', 'cancellation', 'chart_prepared'))
      .default(['confirmation', 'waitlist_movement', 'cancellation'])
  }).optional()
});

